# TODO: Dictionaries are a way to overcome the limitations that we
# in lists or tuples. We can define our indexes the way we like
# unlike standard way of defining it. The main way of defining
# is through key and values.

myDictOne = {
    1: "Ameet",
    2: 5,
    3: 7.5
}

print(myDictOne)
print(myDictOne[1])
print(myDictOne[2])

# TODO: We can print all the keys are values as in the following way
print(myDictOne.keys())
print(myDictOne.values())