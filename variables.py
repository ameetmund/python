# Variables #

# Python is a top down programming language #
# It executes the code from top to down #
# In this example we have assigned first and last names to two variables #
# Then the variables are printed. But if the order is changed where the print #
# will happen first, then it will not be able to recognize the variables #
first_name = 'Steve'
